# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário. 

l = float(input("Digite um lado do quadrado para calcular a area: "))
area = l**2
dobro_area = area * 2
print(f"O quadrado possui lados iguais de: {l}, com area de: {area}, e o dobro dela é: {dobro_area}")
